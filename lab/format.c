void FormatDisk()
{
	//Set up sector zero
	REG_DMASOURCE = (int32_t)format_0;
	REG_DMATARGET = (int32_t)DISKRAM;
	REG_DMALENGTH = 512;
	REG_DMACONTROL = 0x07;

	//Write sector zero
	REG_DISKSECTOR = 0;
	REG_DISKCONTROL = DCTL_WRITENOW;

	//Set up blankness
	REG_DMASOURCE = 0;
	REG_DMATARGET = (int32_t)DISKRAM;
	REG_DMALENGTH = 512;
	REG_DMACONTROL = 0x0D;

	//Write all blank sectors
	for (int32_t i = 1; i < 256; i++)
	{
		REG_DISKSECTOR = i;
		REG_DISKCONTROL = DCTL_WRITENOW;
	}

	//Set up blank chains
	*(int32_t*)DISKRAM = 0xF0FFFF00;

	//Write blank chains
	REG_DISKSECTOR = 1;
	REG_DISKCONTROL = DCTL_WRITENOW;
	REG_DISKSECTOR = 10;
	REG_DISKCONTROL = DCTL_WRITENOW;

	//And we're done!
}
